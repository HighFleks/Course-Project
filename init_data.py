async def reset_database():
    """Полная очистка таблиц приложения. Пользователей тоже стираем."""
    async with engine.begin() as conn:
        # Сносим все данные одним TRUNCATE с CASCADE — порядок не важен.
        await conn.execute(text(
            "TRUNCATE TABLE "
            "recipe_ingredients, favorite_recipes, shopping_list_items, "
            "user_inventory, barcode_cache, recipes, ingredients, users "
            "RESTART IDENTITY CASCADE"
        ))
    print("🧹 База данных очищена.")


def validate_categories():
    """Проверяем что в RECIPES не закралась категория вне справочника."""
    invalid = {r["category"] for r in RECIPES if r["category"] not in RECIPE_CATEGORIES}
    if invalid:
        print(f"❌ В RECIPES найдены неизвестные категории: {invalid}")
        print(f"   Допустимые категории: {sorted(RECIPE_CATEGORIES)}")
        sys.exit(1)


async def init_data():
    validate_categories()
    async with AsyncSessionLocal() as db:
        # 1. Тестовый пользователь
        test_user_email = "demo@example.com"
        result = await db.execute(select(User).where(User.email == test_user_email))
        user = result.scalar_one_or_none()
        if not user:
            user = User(
                email=test_user_email,
                hashed_password=get_password_hash("demo123")
            )
            db.add(user)
            await db.commit()
            await db.refresh(user)
            print(f"✅ Создан тестовый пользователь: {test_user_email} / demo123")
        else:
            print(f"ℹ️  Пользователь {test_user_email} уже существует")

        # 2. Ингредиенты
        ingredient_map: dict[str, Ingredient] = {}
        new_ingredients = 0
        for name, unit in INGREDIENTS:
            result = await db.execute(select(Ingredient).where(Ingredient.name == name))
            ing = result.scalar_one_or_none()
            if not ing:
                ing = Ingredient(name=name, unit=unit)
                db.add(ing)
                await db.flush()
                new_ingredients += 1
            ingredient_map[name] = ing
        await db.commit()
        print(f"✅ Ингредиентов в базе: {len(ingredient_map)} (новых добавлено: {new_ingredients})")

        # 3. Рецепты
        new_recipes = 0
        skipped = 0
        for rec_data in RECIPES:
            existing = await db.execute(
                select(Recipe).where(
                    Recipe.name == rec_data["name"],
                    Recipe.created_by_user_id == user.id,
                )
            )
            if existing.scalar_one_or_none():
                skipped += 1
                continue

            recipe = Recipe(
                name=rec_data["name"],
                description=rec_data["description"],
                instructions=rec_data["instructions"],
                category=rec_data["category"],
                is_public=True,
                created_by_user_id=user.id,
            )
            db.add(recipe)
            await db.flush()

            for ing_name, qty in rec_data["ingredients"]:
                ing = ingredient_map.get(ing_name)
                if not ing:
                    print(f"  ⚠️  Ингредиент «{ing_name}» не найден, пропускаю в рецепте «{rec_data['name']}»")
                    continue
                db.add(RecipeIngredient(
                    recipe_id=recipe.id,
                    ingredient_id=ing.id,
                    quantity=qty,
                ))
            await db.commit()
            new_recipes += 1

        print(f"✅ Рецептов добавлено: {new_recipes}, пропущено (уже существуют): {skipped}")
        print("\n🎉 Инициализация тестовых данных завершена!")
        print(f"   Логин: {test_user_email}")
        print("   Пароль: demo123")


async def main():
    if "--reset" in sys.argv:
        print("⚠️  Запущена очистка базы (флаг --reset).")
        await reset_database()
    await init_data()


if __name__ == "__main__":
    asyncio.run(main())
